func (c Colors) displayColor() *color.RGBA {
	return &c.Colors[c.DisplayColor]
}

func DisplayColorByName(colorName string) *color.RGBA {
	switch strings.ToLower(colorName) {
	case SHIBAFU.Name:
		return SHIBAFU.displayColor()
	case MOMIJI.Name:
		return MOMIJI.displayColor()
	case SORA.Name:
		return SORA.displayColor()
	case ICHOU.Name:
		return ICHOU.displayColor()
	case AJISAI.Name:
		return AJISAI.displayColor()
	case KURO.Name:
		return KURO.displayColor()
	default:
		return nil
	}
}
